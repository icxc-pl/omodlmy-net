    <h2>2. Modlitwa i intencje</h2>


    <h3>2.1. Chcę się pomodlić, ale nie wiem, czy muszę modlić się jakąś konkretną modlitwą?</h3>
    <p>
      Nie - to od Ciebie zależy jakiej "broni" użyjesz. Jeżeli masz czas i chcesz np. odmówić brewiarz - proszę bardzo.
      Jeżeli już i tak w wielu intencjach się modlisz, wystarczy jakaś naprawdę prosta modlitwa czy może nawet
      krótka szczera prośba do Boga.
    </p>


    <h3>2.2. A co jeżeli nie umiem się modlić? Dawno tego nie robiłem/am...</h3>
    <p>
      Myślę, że sam Święty Paweł lepiej odpowie na to pytanie:
    </p>
    <blockquote>
      Na szczęście podobnie także Duch przychodzi z pomocą naszej słabości. Gdy bowiem nie umiemy się modlić tak,
      jak trzeba, sam Duch przyczynia się za nami w błaganiach, których nie można wyrazić słowami. Ten zaś, który
      przenika serca, zna zamiar Ducha, [wie], że przyczynia się za świętymi zgodnie z wolą Bożą.
      <br>
      <i>Rz 8,26–27</i>
    </blockquote>
    <p>
      Polecam też
      <a href="http://filokalia.pl/modlitwa-serca-cz-5-sam-duch-modli-sie-w-nas/" target="noopener">
        artykuł z Filokalia o modlitwie serca
      </a>.
    </p>


    <h3>2.3. Chcę się za kogoś pomodlić - czy muszę zrobić coś szczególnego?</h3>
    <p>
      Pod każdą intencją jest pasek, który dzieli się na dwie części: po lewej stronie wyświetlona jest informacja ile
      osób dołączyło do modlitwy w tej intencji, natomiast po prawej stronie znajduje się przycisk <b>Dołączam</b>.
      <img src="/img/intencja-przyklad.png" alt="Przykładowa intencja" style="display: block;" />
      Kliknięcie w ten przycisk deklaruje - jak nazwa wskazuje - chęć dołączenia do modlitwy w tej intencji.
      Nie musisz tego robić, ale zachęcam ponieważ jest to fajne uczucie jak się wie, że ktoś się modli w mojej intencji.
      Dlatego odwagi! :)
    </p>


    <h2>3. Pomoc</h2>


    <h3>3.1. Jak zainstalować tę aplikację na telefonie? Dlaczego nie ma jej na Google Play / Apple Store ?</h3>
    <p>
      Aplikacji nie trzeba instalować w takim sensie jak robimy to przez sklep. Progresywne aplikacje internetowe
      (w skrócie PWA - tak jak wspomniałem wyżej) po prostu przypina się na pulpit.
      Tak przypięta aplikacja jest już "zainstalowana" i gotowa do użycia. Jednak żeby z niej korzystać potrzebne jest
      połączenie do Internetu (bez obaw - aplikacja nie pobiera go dużo).
      Dlatego też nie ma jej w żadnej sklepie, bo de facto nie trzeba jej ściągać, nadawać praw itd. Wystarczy wejść
      na stronę omodlmy.net i z dodać do pulpitu - i już! :)
      <br>
      Instalacja (przypięcie) aplikacji jest aktalnie obsługiwane w następującej konfiguracji:
    </p>
