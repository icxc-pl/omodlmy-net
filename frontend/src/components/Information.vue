    <h2>1. O projekcie</h2>


    <h3>1.1. Co to za projekt?</h3>
    <p>
      omodlmy.net to aplikacja typu <a href="https://pl.wikipedia.org/wiki/Progressive_web_app" target="noopener">PWA</a>,
      która ma zachęcać do modlenia się za siebie nawzajem.
    </p>


    <h3>1.2. Do czego to ma służyć?</h3>
    <p>
      Do modlenia się w intencjach, które są ważne dla każdego z nas, ale także za to, co dzieje się w Internecie -
      by było tam jak najwięcej dobra, które czasem trudno dostrzec.
    </p>


    <h3>1.3. Skąd pomysł?</h3>
    <p>
      W Duszpasterstwie Akademickim "Redemptor" we Wrocławiu, w działalność którego angażowałem się na studiach, często
      ktoś prosił o modlitwe - pisał o tym na grupie na FB, zgłaszał to do skrzynki intencji, wysyłał maila z prośbą.
      Ludzie bardzo często odpowiadali, pisząc komentarz/odpowiedź w postaci "+" - czyli jakby krzyżyka.
      Był to pierwszy znak, że można zrobić to lepiej, a co więcej - nie ma narzędzia stworzonego właśnie w celu
      dodania swojej intencji, tak aby ktoś mógł się w tej konkretnej intencji pomodlić - nie koniecznie znajomy -
      przecież w kościele jako wspólnota rzadko znamy osoby siedzące obok nas, a jednak... modlimy się wspólnie.
    </p>


    <h3>1.4. Co reprezentuje logo?</h3>
    <p>
      Internet często na ikonkach jest przedstawiany w postaci globu oplecionego siecią kabli. A omodlmy.net
      ma być projektem, przez który właśnie dzięki tej sieci kabli popłynie modlitwa. Od razu pomyślałem o różańcu -
      zapragnąłem, żeby w centrum był krzyż - symbol Jezusa, Alfy i Omegi, początku i końca - i z tego krzyża wypływa
      sieć stworzona z różańca. Co więcej te punkty (tajemnice różańca) symbolizują też ludzi, którzy są w różnym
      miejscu na ziemi i mogą modlić się za osobę być może z całkowicie innego miejsca.
    </p>


